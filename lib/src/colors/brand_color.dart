import 'package:flutter/material.dart';

/// A brand Color class containing:
/// * [coolBlue]
/// * [calmBlue]
/// * [white]
///
/// Usage example:
///
/// ```dart
/// Color coolBlue = BrandColor.coolBlue
/// Color calmBlue = BrandColor.calmBlue
/// Color white = BrandColor.white
/// ```
class BrandColor {
  /// Primary Brand Color
  static const Color coolBlue = Color(0XFF667fff);

  /// Secondary Brand Color
  static const Color calmBlue = Color(0XFF3355FF);

  /// Tertiery Brand Color
  static const Color white = Color(0XFFFFFFFF);
}
